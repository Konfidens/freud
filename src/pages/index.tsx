import React, { type FormEvent } from "react";
import Head from "next/head";
import { api } from "~/utils/api";
import { Role, type Message } from "~/interfaces/message";
import SourceComponent from "~/components/sourceComponent";

export default function Home() {
  const [query, setQuery] = React.useState("");
  const [messages, setMessages] = React.useState<Message[]>([]);

  const mutation = api.langchain.conversation.useMutation({
    onError: (error) => {
      console.error(error);
    },
    onSuccess: (message) => {
      setMessages([...messages, message!]);
    },
  });

  const vectorStoreMutation = api.vectorstore.create.useMutation({
    onError: (error) => console.error(error),
    onSuccess: () => console.info("Vector store created"),
  });

  function handleSubmit(event: FormEvent<HTMLFormElement>) {
    event.preventDefault();
    const message = {
      role: Role.User,
      content: query,
    };
    setMessages([...messages, message]);
    mutation.mutate([...messages, message]);
  }

  function createVectorStore() {
    vectorStoreMutation.mutate("dummy text");
  }

  return (
    <>
      <Head>
        <title>Freud</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gradient-to-b from-[#2e026d] to-[#15162c]">
        <div className="container flex flex-col items-center justify-center gap-12 px-4 py-16 ">
          <h1 className="text-5xl font-extrabold tracking-tight text-white sm:text-[5rem]">
            Freud
          </h1>
          <button className="bg-white" onClick={createVectorStore}>
            Generer vektor-database
          </button>
          <div>
            {messages.map((message, idx) => {
              return message.role === Role.User ? (
                <p className="pt-5 text-white" key={idx}>
                  {message.content}
                </p>
              ) : (
                <div key={idx}>
                  <p
                    className="pt-2 text-red-500"
                    key={"reply-" + idx.toString()}
                  >
                    {message.content}
                  </p>
                  <p
                    className="bold pt-2 font-bold text-green-900"
                    key={"sources-" + idx.toString()}
                  >
                    Kilder
                  </p>
                  <ul
                    className="list-disc text-green-900"
                    key={"source-list-" + idx.toString()}
                  >
                    {message.sources ? (
                      message.sources.map((source, listId) => {
                        return (
                          <li
                            key={
                              "source-list-" +
                              idx.toString() +
                              "-element-" +
                              listId.toString()
                            }
                          >
                            {source.title} av {source.author}, s.{" "}
                            {source.location.pageNr} (linje{" "}
                            {source.location.lineFrom}-{source.location.lineTo})
                            <br />
                            <SourceComponent
                              text={source.content}
                            ></SourceComponent>
                          </li>
                        );
                      })
                    ) : (
                      <li>No sources available</li>
                    )}
                  </ul>
                </div>
              );
            })}
          </div>
        </div>
        <form onSubmit={handleSubmit} className="mb-2 block">
          <input
            className="block w-full p-4"
            type="text"
            id="querySearch"
            value={query}
            onChange={(event) => {
              setQuery(event.target.value);
            }}
          />
        </form>
      </main>
    </>
  );
}
