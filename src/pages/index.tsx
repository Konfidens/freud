import React, { type FormEvent } from "react";
import Head from "next/head";
import { api } from "~/utils/api";
import { Role, type Message } from "~/interfaces/chat";

export default function Home() {
  const [query, setQuery] = React.useState("");
  const [messages, setMessages] = React.useState<Message[]>([]);

  const mutation = api.openai.chat.useMutation({
    onError: (error) => {
      console.error(error);
    },
    onSuccess: (message) => {
      setMessages([...messages, message!]);
    },
  });

  function handleSubmit(event: FormEvent<HTMLFormElement>) {
    event.preventDefault();
    const message = {
      role: Role.User,
      content: query,
    };
    setMessages([...messages, message]);
    mutation.mutate([...messages, message]);
  }

  return (
    <>
      <Head>
        <title>Freud</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gradient-to-b from-[#2e026d] to-[#15162c]">
        <div className="container flex flex-col items-center justify-center gap-12 px-4 py-16 ">
          <h1 className="text-5xl font-extrabold tracking-tight text-white sm:text-[5rem]">
            Freud
          </h1>
          <form onSubmit={handleSubmit}>
            <label htmlFor="querySearch">Input box;</label>
            <input
              type="text"
              id="querySearch"
              value={query}
              onChange={(event) => {
                setQuery(event.target.value);
              }}
            />
          </form>
          <div>
            {messages.map((message, idx) => {
              return message.role === Role.User ? (
                <p className="text-white" key={idx}>
                  {message.content}
                </p>
              ) : (
                <p className="text-red-500" key={idx}>
                  {message.content}
                </p>
              );
            })}
          </div>
        </div>
      </main>
    </>
  );
}
